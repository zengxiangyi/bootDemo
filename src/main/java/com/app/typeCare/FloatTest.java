package com.app.typeCare;

public class FloatTest {

	public static void main(String[] args) {
		float x=8.70f;
		float y=4.35f;
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(y*6);
		System.out.println(x/2.0);
		System.out.println(x/3.0f);
	}

}
